# Bitwise operators in Python

#   &         --    AND         -- ex: 1 & 0 yields 0
#   | (pipe)  --    OR          -- ex: 1 | 0 yields 1
#   ^         --    XOR         -- ex: 1 ^ 1 yields 0
#   ~         --    NOT
#   <<        --    shift left  -- ex: 101 << 2 yields 10100
#   >>        --    shift right -- ex: 10100 >> 2 yields 101 
